heroes[1] = DEMON_HUNTER
heroes[2] = KEEPER
heroes[3] = MOON_BABE
heroes[4] = WARDEN
elseif raceid == RACE_UNDEAD then
heroes[1] = DEATH_KNIGHT
heroes[2] = DREAD_LORD
heroes[3] = LICH
heroes[4] = CRYPT_LORD
